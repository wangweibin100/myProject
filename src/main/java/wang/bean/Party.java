package main.java.wang.bean;

public class Party {
	// 会议室ID
	private String partyId = "";
	// 会议室预定日期
	private String partyDate = "";
	// 会议室开始时间
	private String startTime = "";
	// 会议室结束时间
	private String endTime = "";
	// 预约者姓名
	private String userName = "";
	// 预约者电话
	private String telNo = "";
	// 预约者邮箱
	private String emailNo = "";
	// 预约原因
	private String resionId = "";
	// 预约内容描述
	private String comment = "";
	/**
	 * @return the partyId
	 */
	public String getPartyId() {
		return partyId;
	}
	/**
	 * @param partyId the partyId to set
	 */
	public void setPartyId(String partyId) {
		this.partyId = partyId;
	}
	/**
	 * @return the partyDate
	 */
	public String getPartyDate() {
		return partyDate;
	}
	/**
	 * @param partyDate the partyDate to set
	 */
	public void setPartyDate(String partyDate) {
		this.partyDate = partyDate;
	}
	/**
	 * @return the startTime
	 */
	public String getStartTime() {
		return startTime;
	}
	/**
	 * @param startTime the startTime to set
	 */
	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}
	/**
	 * @return the endTime
	 */
	public String getEndTime() {
		return endTime;
	}
	/**
	 * @param endTime the endTime to set
	 */
	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}
	/**
	 * @return the userName
	 */
	public String getUserName() {
		return userName;
	}
	/**
	 * @param userName the userName to set
	 */
	public void setUserName(String userName) {
		this.userName = userName;
	}
	/**
	 * @return the telNo
	 */
	public String getTelNo() {
		return telNo;
	}
	/**
	 * @param telNo the telNo to set
	 */
	public void setTelNo(String telNo) {
		this.telNo = telNo;
	}
	/**
	 * @return the emailNo
	 */
	public String getEmailNo() {
		return emailNo;
	}
	/**
	 * @param emailNo the emailNo to set
	 */
	public void setEmailNo(String emailNo) {
		this.emailNo = emailNo;
	}
	/**
	 * @return the resionId
	 */
	public String getResionId() {
		return resionId;
	}
	/**
	 * @param resionId the resionId to set
	 */
	public void setResionId(String resionId) {
		this.resionId = resionId;
	}
	/**
	 * @return the comment
	 */
	public String getComment() {
		return comment;
	}
	/**
	 * @param comment the comment to set
	 */
	public void setComment(String comment) {
		this.comment = comment;
	}
}
